public class tugas4 {
    public static void main(String[] args) {
        int x = 22;
        int y = 33;

        //penjumlahan
        int sum = x + y;
        System.out.println("Hasil penjumlahan x dan y adalah : " + sum);

        //pengurangan
        int difference = x -y;
        System.out.println("Hasil pengurangan x dan y adalah : " + difference);

        //perkalian
        int product = x * y;
        System.out.println("Hasil perkalian x dan y adalah :" + product);

        //pembagian
        int quotient = x / y;
        System.out.println(" Hasil pembagian x dan y adalah : " + quotient);
    }
}
